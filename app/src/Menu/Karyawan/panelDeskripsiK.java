    private void BPecatKaryawanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPecatKaryawanActionPerformed
        // TODO add your handling code here:
        int konfirmasi=JOptionPane.showConfirmDialog(null, "Pecat "+LNamaKaryawan.getText()+"? \n Data Karyawan Akan Tetap Tersimpan ", "Pecat Karyawan", JOptionPane.YES_NO_OPTION);
        if(konfirmasi==JOptionPane.YES_OPTION){
            try {
                statusAktif= "TIDAK AKTIF";
                 koneksi.updateData("UPDATE karyawan set Tanggal_Keluar_Kerja = '"+ fungsi.gettanggal()+"', Status_Aktif='"+statusAktif+"' where id_karyawan = '"+idKaryawan+"'");
                 koneksi.updateData("DELETE FROM `karyawan.akunlogin` WHERE `ID_Karyawan` ='"+idKaryawan+"'");
                JOptionPane.showMessageDialog(null, "KARYAWAN TELAH DIPECAT \n\n DATA KARYAWAN MASIH BISA DILIHAT","Pecat Karyawan",JOptionPane.INFORMATION_MESSAGE);
                panelidentitas.JS.setViewportView(new panelDeskripsiK());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_BPecatKaryawanActionPerformed

    private void BKerjaKembaliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BKerjaKembaliActionPerformed
        // TODO add your handling code here:
        JDateChooser JDS = new JDateChooser();
        int konfirmasi=JOptionPane.showConfirmDialog(null, "Kerjakan Kembali "+LNamaKaryawan.getText()+"?", "Pekerjakan Kembali Karyawan", JOptionPane.YES_NO_OPTION);
        if(konfirmasi==JOptionPane.YES_OPTION){
            int gnt= JOptionPane.showOptionDialog(this, new Object[] {"MASUKAN TANGGAL LAHIR \n\n",JDS}, null, JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[] {"OK", "CANCEL"}, null);
            if(gnt==JOptionPane.OK_OPTION){
                try {
                    statusAktif = "AKTIF";
                    username        = idKaryawan;
                    password        = String.valueOf(formatPassword.format(JDS.getDate()));
                     koneksi.updateData("UPDATE karyawan set Tanggal_Masuk_Kerja = '"+ fungsi.gettanggal()+"',Tanggal_Keluar_Kerja = null, Status_Aktif='"+statusAktif+"' where id_karyawan = '"+idKaryawan+"'");
                     koneksi.updateData("INSERT INTO `karyawan.akunlogin` (username, password, id_karyawan) values "
                            + "('"+username+"','"+password+"','"+idKaryawan+"')");
                    JOptionPane.showMessageDialog(null, "Karyawan Telah Bekerja Kembali","Pekerjakan Kembali Karyawan",JOptionPane.INFORMATION_MESSAGE);
                    panelidentitas.JS.setViewportView(new panelDeskripsiK());
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            
        }
        }
    }//GEN-LAST:event_BKerjaKembaliActionPerformed


